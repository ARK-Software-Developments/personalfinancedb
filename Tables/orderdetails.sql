/* Structure for the `orderdetails` table : */

CREATE TABLE `orderdetails` (
  `id` INTEGER NOT NULL AUTO_INCREMENT,
  `ordersid` INTEGER DEFAULT NULL,
  `brand` VARCHAR(45) COLLATE utf8mb4_0900_ai_ci DEFAULT NULL,
  `productdetails` VARCHAR(200) COLLATE utf8mb4_0900_ai_ci DEFAULT NULL,
  `description` VARCHAR(200) COLLATE utf8mb4_0900_ai_ci DEFAULT NULL,
  `productcode` VARCHAR(45) COLLATE utf8mb4_0900_ai_ci DEFAULT NULL,
  `quantity` INTEGER DEFAULT NULL,
  `unitprice` DECIMAL(10,2) DEFAULT NULL,
  `subtotal` DECIMAL(10,2) DEFAULT NULL,
  `to` VARCHAR(45) COLLATE utf8mb4_0900_ai_ci DEFAULT NULL,
  `statusid` INTEGER DEFAULT NULL,
  PRIMARY KEY USING BTREE (`id`),
  KEY `fx_ordersid_orders_idx` USING BTREE (`ordersid`),
  KEY `fx_statusid_statud_idx` USING BTREE (`statusid`),
  CONSTRAINT `fx_ordersid_orders` FOREIGN KEY (`ordersid`) REFERENCES `orders` (`id`),
  CONSTRAINT `fx_statusid_statud` FOREIGN KEY (`statusid`) REFERENCES `status` (`id`)
) ENGINE=InnoDB
AUTO_INCREMENT=48 ROW_FORMAT=DYNAMIC CHARACTER SET 'utf8mb4' COLLATE 'utf8mb4_0900_ai_ci';

