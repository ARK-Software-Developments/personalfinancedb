/* Structure for the `payments` table : */

CREATE TABLE `payments` (
  `id` INTEGER NOT NULL AUTO_INCREMENT,
  `registrationdate` DATETIME DEFAULT NULL,
  `dateofpayment` DATETIME DEFAULT NULL,
  `registrationcode` VARCHAR(45) COLLATE utf8mb4_0900_ai_ci DEFAULT NULL,
  `paymentresourceid` INTEGER DEFAULT NULL COMMENT 'entitiesid',
  `paymenttype` VARCHAR(45) COLLATE utf8mb4_0900_ai_ci DEFAULT NULL,
  `budgetedamount` DECIMAL(10,2) DEFAULT NULL,
  `amountpaid` DECIMAL(10,2) DEFAULT NULL,
  `reasonforpayment` INTEGER DEFAULT NULL COMMENT 'typeofexpenseid',
  PRIMARY KEY USING BTREE (`id`),
  KEY `fx_entities_entitiesid_idx` USING BTREE (`paymentresourceid`),
  KEY `fx_typeofexpense_typeofexpenseid_idx` USING BTREE (`reasonforpayment`),
  CONSTRAINT `fx_entities_entitiesid` FOREIGN KEY (`paymentresourceid`) REFERENCES `entities` (`id`),
  CONSTRAINT `fx_typeofexpense_typeofexpenseid` FOREIGN KEY (`reasonforpayment`) REFERENCES `typeofexpense` (`id`)
) ENGINE=InnoDB
AUTO_INCREMENT=43 ROW_FORMAT=DYNAMIC CHARACTER SET 'utf8mb4' COLLATE 'utf8mb4_0900_ai_ci';

